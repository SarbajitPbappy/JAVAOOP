    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String txno = tNo.getText();
        String txname = tName.getText();
        String txstart = tStart.getText();
        String txdest = tDest.getText();
        String txprice = tPrice.getText();
        try {
            pst = con.prepareStatement("insert into traindetails(`Train_No`,`Train_Name`,`Start`,`Destination`,`Ticket_Price`) values(?,?,?,?,?)");
            pst.setString(1, txno);
            pst.setString(2, txname);
            pst.setString(3, txstart);
            pst.setString(4, txdest);
            pst.setString(5, txprice);
            int k = pst.executeUpdate();
            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Added Successfully");
            } else {
                JOptionPane.showMessageDialog(this, "Failed");
            }
            tNo.setText("");
            tName.setText("");
            tStart.setText("");
            tDest.setText("");
            tPrice.setText("");
            tNo.requestFocus();
            TrainDetails();
        } catch (SQLException ex) {
            Logger.getLogger(addTrain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

