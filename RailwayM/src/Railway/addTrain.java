    public void TrainDetails() {
        try {
            pst = con.prepareStatement("select * from traindetails");
            rs = pst.executeQuery();
            ResultSetMetaData rsd;
            rsd = (ResultSetMetaData) rs.getMetaData();
            int c;
            c = rsd.getColumnCount();
            DefaultTableModel d = (DefaultTableModel) jTable1.getModel();
            d.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                for (int i = 0; i <= c; i++) {
                    v.add(rs.getString("Train_No"));
                    v.add(rs.getString("Train_Name"));
                    v.add(rs.getString("Start"));
                    v.add(rs.getString("Destination"));
                    v.add(rs.getString("Ticket_Price"));
                }
                d.addRow(v);
            }

        } catch (SQLException ex) {
            Logger.getLogger(addTrain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

